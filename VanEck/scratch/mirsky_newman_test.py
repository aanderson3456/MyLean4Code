# Just a quick check to ensure Mirsky-Newman applies to Z_P as well.
# If Z_P is partitioned into progressions with steps d_i, then by repeating with period P,
# we get a partition of Z into progressions with steps d_i.
# Since the steps d_i are distinct, this violates Mirsky-Newman.
# Therefore, it is impossible for Z_P as well!
print("Mirsky-Newman applies.")
